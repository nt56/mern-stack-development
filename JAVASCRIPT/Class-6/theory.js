// Synchronous:
// It is the default behaviour of our javascript program
// When we have got multiple functions to be executed, sync behaviour makes the fucntion
// get executed one by one
// Until the previous hasn't completed it functioning, the next function has to keep waiting
// Due to this the efficiency of the code reduces and it also hampers the user
// experience and the program performance

// Asynchronous:
// It is used to counter the effect of synchronous behaviour
// It this the function which will take time makes sure the other functions get executed.
// Due to this the program runs more efficiently and the memory is utilized in a better way
// We can acheive Asynchronous behaviour by use of following methods:
// async and await
// promises
// setTimeout
// setInterval
